func threatEq(expected, received []Threat) bool {
	if len(expected) != len(received) {
		return false
	}
	for key, value := range received {
		if expected[key].owner != value.owner {
			return false
		} else if expected[key].size != value.size {
			return false
		} else if expected[key].status != value.status {
			return false
		} else if board.DiffCoords(expected[key].corner, value.corner) == false {
			return false
		} else if board.DiffCoords(expected[key].positions, value.positions) == false {
			return false
		}
	}
	return true
}

